    [ "0111111","0000011", "0111111", "0111111", "0110011", "0111111", "0111111", "0111111", "0111111", "0111111" ], 
    [ "0110011","0000011", "0000011", "0000011", "0110011", "0110000", "0110000", "0000011", "0110011", "0110011" ], 
